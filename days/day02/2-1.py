ranges = input_ranges.split(",")
count_invalid_ids = 0

for i in ranges:
    j = (i.split("-")[0])
    o = (i.split("-")[1])


    for k in range(int(j),int(o)+1):
        if len(str(k)) == 2:
            if (str(k)[0]) == (str(k)[1]):
                count_invalid_ids += k
        else:
            l = len(str(k))//2
            if (str(k)[:l]) == (str(k)[l:]):
                count_invalid_ids += k

print(count_invalid_ids)
